        // Variables
        String [][] movieChoices = {};
        
        String [] movieTitle = new String [4];
        String [] movieImage = new String [4];
        String [] movieRating = new String [4];
        
       
        
        
        // Make sure the user has selected a rating before proceeding
        if (!(cbxG.isSelected()) && !(cbxPG.isSelected()) && !(cbxPG13.isSelected())) {
            // Indicate that the user needs to select a rating
            cbxG.requestFocus();

            // exit
            return;
        }
        
        // Add G rated movies to the possible movie choices
        if (cbxG.isSelected()) {
            movieChoices = new String [gMovies.length][4];
            System.arraycopy(gMovies, 0, movieChoices, 0, movieChoices.length);
        }
        
        // Add PG rated movies to the possible movie choices
        if (cbxPG.isSelected()) {
            movieChoices = append(pgMovies, movieChoices);
        }

        // Add PG-13 rated movies to the possible movie choices
        if (cbxPG13.isSelected()) {
            movieChoices = append(pg13Movies, movieChoices);
        }
        
        // Select four random movies from the possible movie choices
        for (int i = 0; i < 4; i ++) {
            int randomPos;
            do {
                randomPos = (int)Math.round(Math.random() * (movieChoices.length - 1));
            } while (isIn(movieChoices[randomPos][0], movieTitle));
            // Assign variables for each movie
            movieTitle[i] = movieChoices[randomPos][0];
            movieImage[i] = movieChoices[randomPos][1];
            movieLink[i] = movieChoices[randomPos][2];
            movieRating[i] = movieChoices[randomPos][3];
        }
        
        // Output all the movie posters
        lblPoster1.setIcon(new javax.swing.ImageIcon(getClass().getResource(movieImage[0])));
        lblPoster2.setIcon(new javax.swing.ImageIcon(getClass().getResource(movieImage[1])));
        lblPoster3.setIcon(new javax.swing.ImageIcon(getClass().getResource(movieImage[2])));
        lblPoster4.setIcon(new javax.swing.ImageIcon(getClass().getResource(movieImage[3])));
        
        // Output the movie names
        lblMovie1.setText(movieTitle[0]);
        lblMovie2.setText(movieTitle[1]);
        lblMovie3.setText(movieTitle[2]);
        lblMovie4.setText(movieTitle[3]);
        
        // Output the movie ratings
        lblRating1.setText(movieRating[0]);
        lblRating2.setText(movieRating[1]);
        lblRating3.setText(movieRating[2]);
        lblRating4.setText(movieRating[3]);
        
        // After being clicked, disable this button and the rating checkboxes
        btnGenerateMovies.setEnabled(false);
        cbxG.setEnabled(false);
        cbxPG.setEnabled(false);
        cbxPG13.setEnabled(false);
