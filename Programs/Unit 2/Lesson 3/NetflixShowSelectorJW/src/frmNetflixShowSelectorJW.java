    // Append an array to another array
    static String [][] append(String [][] newArray, String [][] originalArray) {
        // Variables
        int originalArraySize = originalArray.length;
        int newArraySize = newArray.length;
        
        String [][] tempArray = new String [newArraySize + originalArraySize][4];

        System.arraycopy(originalArray, 0, tempArray, 0, originalArraySize);
        System.arraycopy(newArray, 0, tempArray, originalArraySize, newArraySize);
        originalArray = new String [newArraySize + originalArraySize][4];
        System.arraycopy(tempArray, 0, originalArray, 0, newArraySize + originalArraySize);
        
        return originalArray;
    }
    
